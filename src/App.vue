<template>
  <div id="app">
    <router-view> </router-view>
  </div>
</template>

<script>
export default {
  Name: "app",
  provide() {
    return {
      reload: this.reload,
    };
  },
  data() {
    return {
      isRouterAlive: true,
    };
  },
  methods: {
    reload() {
      this.isRouterAlive = false;
      this.$nextTick(function () {
        this.isRouterAlive = true;
      });
    },
  },
};
</script>

<style>
html,
body,
#app {
  height: 100%;
}
</style>

